#Create an Empty List
numbers = []

#Each line adds a new element 4,5,10
numbers.append(4)
numbers.append(5)
numbers.append(10)

#Print the list to show it's contents
print(numbers)

#Use the remove function to remove the number 5 
numbers.remove(5)
print(numbers)

#Use the pop function to remove the first element
numbers.pop(0)
print(numbers)